import { API } from '../base'

export interface Geo {
	lat: string
	lng: string
}

export interface Address {
	city: string
	geo: Geo
	street: string
	suite: string
	zipcode: string
}

export interface Company {
	bs: string
	catchPhrase: string
	name: string
}

export interface User {
	address: Address
	company: Company
	email: string
	id: number
	name: string
	phone: string
	username: string
	website: string
}

const userApi = API.injectEndpoints({
	endpoints: (build) => ({
		getUser: build.query<User, string>({
			query: (id) => `users/${id}`,
		}),
	}),
	overrideExisting: true,
})

export const { useLazyGetUserQuery } = userApi
